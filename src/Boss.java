public class Boss {
    private int health = 200;
    private int damage = 50;
    private String defence = "Spher";

    public int setHealth() {
        return health;
    }

    public int getHealth() {
        return health;
    }

    public int setDamage() {
        return damage;
    }

    public int getDamage() {
        return damage;
    }

    public String setDefence() {
        return defence;
    }

    public String getDefence() {
        return defence;
    }
}
